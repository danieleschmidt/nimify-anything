class DockerfileGenerator:
    """Generate an optimized multi-stage Dockerfile for the NIM service."""

    def __init__(self, metadata: ModelMetadata):
        self.meta = metadata

    def generate(self, output_dir: Path) -> Path:
        output_dir.mkdir(parents=True, exist_ok=True)
        df_path = output_dir / "Dockerfile"
        df_path.write_text(self._render(), encoding="utf-8")
        return df_path

    def _render(self) -> str:
        name = self.meta.name
        return textwrap.dedent(f"""\
            # syntax=docker/dockerfile:1.4
            # ─── Stage 1: build/install dependencies ────────────────────────────────────
            FROM python:3.12-slim AS builder

            WORKDIR /build

            # Install build tools
            RUN apt-get update && apt-get install -y --no-install-recommends \\
                    gcc \\
                && rm -rf /var/lib/apt/lists/*

            COPY requirements.txt .
            RUN pip install --no-cache-dir --prefix=/install -r requirements.txt


            # ─── Stage 2: runtime image ──────────────────────────────────────────────────
            FROM python:3.12-slim AS runtime

            LABEL org.opencontainers.image.title="{name}" \\
                  org.opencontainers.image.description="NVIDIA NIM-style inference service" \\
                  org.opencontainers.image.licenses="MIT"

            WORKDIR /app

            # Copy installed packages from builder
            COPY --from=builder /install /usr/local

            # Copy application and model
            COPY app.py .
            COPY model.onnx ./model.onnx

            # Non-root user for security
            RUN useradd -r -u 1000 nimuser && chown -R nimuser:nimuser /app
            USER nimuser

            EXPOSE 8000

            HEALTHCHECK --interval=30s --timeout=5s --start-period=10s --retries=3 \\
                CMD python -c "import urllib.request; urllib.request.urlopen('http://localhost:8000/health')"

            CMD ["python", "-m", "uvicorn", "app:app", \\
                 "--host", "0.0.0.0", "--port", "8000", \\
                 "--workers", "1", "--no-access-log"]
        """)
