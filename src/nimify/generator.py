class NIMServiceGenerator:
    """Generate a FastAPI application stub from model metadata."""

    def __init__(self, metadata: ModelMetadata):
        self.meta = metadata

    def generate(self, output_dir: Path) -> Path:
        output_dir.mkdir(parents=True, exist_ok=True)
        app_path = output_dir / "app.py"
        app_path.write_text(self._render(), encoding="utf-8")
        return app_path

    def _render(self) -> str:
        name = self.meta.name
        inputs_fields = "\n".join(_tensor_to_pydantic_field(t) for t in self.meta.inputs)
        outputs_fields = "\n".join(_tensor_to_pydantic_field(t) for t in self.meta.outputs)

