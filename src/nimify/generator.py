class OpenAPIGenerator:
    """Generate an OpenAPI 3.0 spec JSON from model I/O signatures."""

    def __init__(self, metadata: ModelMetadata):
        self.meta = metadata

    def generate(self, output_dir: Path) -> Path:
        output_dir.mkdir(parents=True, exist_ok=True)
        spec_path = output_dir / "openapi.json"
        spec_path.write_text(json.dumps(self._build(), indent=2), encoding="utf-8")
        return spec_path

    def _tensor_schema(self, t: TensorInfo) -> Dict:
        ndim = len(t.shape) if t.shape else 1
        inner = _dtype_to_openapi(t.dtype)
        schema: Dict = inner
        for i in range(ndim):
            dim = t.shape[-(i + 1)] if t.shape else None
            item_schema = schema
            schema = {"type": "array", "items": item_schema}
            if dim is not None:
                schema["minItems"] = dim
                schema["maxItems"] = dim
        schema["description"] = f"dtype={t.dtype}, shape={t.shape_str()}"
        return schema

    def _body_schema(self, tensors: List[TensorInfo]) -> Dict:
        return {
            "type": "object",
            "required": [t.name for t in tensors],
            "properties": {t.name: self._tensor_schema(t) for t in tensors},
        }

    def _build(self) -> Dict:
        name = self.meta.name
        return {
            "openapi": "3.0.0",
            "info": {
                "title": f"{name} NIM API",
                "version": "0.2.0",
                "description": (
                    f"NVIDIA NIM-style inference microservice for `{name}`. "
                    f"ONNX opset {self.meta.opset_version}."
                ),
            },
            "servers": [{"url": "http://localhost:8000", "description": "Local dev"}],
            "paths": {
                "/predict": {
                    "post": {
                        "summary": "Run model inference",
                        "operationId": "predict",
                        "tags": ["inference"],
                        "requestBody": {
                            "required": True,
                            "content": {
                                "application/json": {
                                    "schema": {
                                        "$ref": "#/components/schemas/PredictRequest"
                                    }
                                }
                            },
                        },
                        "responses": {
                            "200": {
                                "description": "Prediction result",
                                "content": {
                                    "application/json": {
                                        "schema": {
                                            "$ref": "#/components/schemas/PredictResponse"
                                        }
                                    }
                                },
                            },
                            "422": {"description": "Validation error"},
                            "500": {"description": "Internal error"},
                        },
                    }
                },
                "/health": {
                    "get": {
                        "summary": "Health check",
                        "operationId": "health",
                        "tags": ["ops"],
                        "responses": {
                            "200": {"description": "Service healthy"}
                        },
                    }
                },
                "/ready": {
                    "get": {
                        "summary": "Readiness probe",
                        "operationId": "ready",
                        "tags": ["ops"],
                        "responses": {
                            "200": {"description": "Service ready"},
                            "503": {"description": "Not ready"},
                        },
                    }
                },
                "/metrics": {
                    "get": {
                        "summary": "Prometheus metrics",
                        "operationId": "metrics",
                        "tags": ["ops"],
                        "responses": {
                            "200": {
                                "description": "Prometheus text format",
                                "content": {"text/plain": {"schema": {"type": "string"}}},
                            }
                        },
                    }
                },
            },
            "components": {
                "schemas": {
                    "PredictRequest": self._body_schema(self.meta.inputs),
                    "PredictResponse": self._body_schema(self.meta.outputs),
                }
            },
        }


