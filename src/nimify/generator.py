def _dtype_to_python(dtype: str) -> str:
    mapping = {
        "float32": "float",
        "float64": "float",
        "float16": "float",
        "bfloat16": "float",
        "int8":  "int",
        "int16": "int",
        "int32": "int",
        "int64": "int",
        "uint8":  "int",
        "uint16": "int",
        "uint32": "int",
        "uint64": "int",
        "bool":   "bool",
        "string": "str",
    }
    return mapping.get(dtype, "float")


def _dtype_to_openapi(dtype: str) -> Dict:
    if "float" in dtype:
        return {"type": "number", "format": "float"}
    if "int" in dtype or dtype == "bool":
        return {"type": "integer"}
    return {"type": "string"}


def _tensor_to_pydantic_field(t: TensorInfo) -> str:
    """Return the Pydantic field declaration for one tensor."""
    py_type = _dtype_to_python(t.dtype)
    shape_comment = t.shape_str()
    # Flatten to a nested List type hint reflecting the ndim
    ndim = len(t.shape) if t.shape else 1
    type_hint = py_type
    for _ in range(ndim):
        type_hint = f"List[{type_hint}]"
    return f'    {t.name}: {type_hint}  # shape {shape_comment}, dtype {t.dtype}'


